export const getVideoById = (id) => videos.find((video) => video.id === id)
export const getVideosByDJ = (djId) => videos.filter((video) => video.djId === djId)
export const getVideosBySeries = (series) => videos.filter((video) => video.series === series)
export const getVideosByGenre = (genre) => videos.filter((video) => video.genres.includes(genre))
export const getVideosByMood = (mood) => videos.filter((video) => video.mood.includes(mood))
export const getLatestVideos = (count = 4) =>
  [...videos].sort((a, b) => new Date(b.date) - new Date(a.date)).slice(0, count)
